#include <newled.hpp>

#include <Arduino.h>

static const uint8_t PIN = PC0;

void Led::device_init()
{
    pinMode(PIN, OUTPUT);
}

void Led::device_set(const bool on)
{
    digitalWrite(PIN, !on);
}
